//******************************************************************************
//***********************Conversor DEC a BCD************************************
//******************************************************************************
unsigned char DEC_TO_HEX (unsigned char dato)
{
    decenasH = dato;
    unidadesH = dato;
//------------------------------------------------------------------------------
    decenasH /= 10;
    decenasH10 = decenasH;
    decenasH = decenas<<4;
//------------------------------------------------------------------------------
    if (dato > 9)
    {
        
        dato -= (10 * decenasH10);
        unidadesH = dato;
    }
    DEC_BCD_RESULTADO = unidadesH + decenasH;
//------------------------------------------------------------------------------    
    return DEC_BCD_RESULTADO;    
}
//******************************************************************************
//***********************Conversor BCD a decimal********************************
//******************************************************************************
void DEC_TO_LCD(unsigned char dato)                                             //ajuste dia
{
    unidades = dato;
    decenas = dato;
    decenas /= 10;
    if (dato >9)
    {
        unidades %= 10;
    }
    if (dato <=0)
    {
        unidades = 0;
        decenas = 0;
    }
    unidades += 0x30;
    decenas += 0x30;
}
//******************************************************************************
//***********************Conversor decimal a BCD********************************
//******************************************************************************
unsigned char BCD_TO_DEC(unsigned char dato)
{
    decenas = dato;
    unidades = dato;
//------------------------------------------------------------------------------    
    decenas = decenas>>4;
    decenas += 0x30;
//------------------------------------------------------------------------------
    if (unidades > 9)
    {
        unidades = unidades<<4;
        unidades /= 0x10;
    }
    unidades += 0x30;
//------------------------------------------------------------------------------    
    return decenas,unidades;
}
